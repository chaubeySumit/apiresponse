package com.gojek.client;

public class UrlWrap {
    private String url1;
    private String url2;

    public String getUrl1() {
        return url1;
    }

    public String getUrl2() {
        return url2;
    }

    public UrlWrap(String url1, String url2) {
        this.url1 = url1;
        this.url2 = url2;
    }

}
